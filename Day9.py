# condiational statement in Python
    # Used to execute certain of code on specific condition

# If conditional statement
    #If statement is the simplest form of a conditional statement. 
    # It executes a block of code if the given condition is true.
age = 20
if age >= 18:
    print("Eligible to vote.")

# Short Hand if
    
    # Short-hand if statement allows us to write a single-line if statement.

age = 19
if age > 18: print("Eligible to Vote.")

# If else condition statement
    
    # if the condition not satisfied then what block of code will be executed and what not.

age = 10
if age <= 12:
    print("Travel for free.")
else:
    print("Pay for ticket.")