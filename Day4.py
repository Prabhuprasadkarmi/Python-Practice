#  Bitwise operator
   
     # Bitwise oprator act on the bits and operate bit-by-bit operation

# Types of the Bitwise operator
    # bitwise NOT
    # bitwise AND
    # bitwise OR
    # bitwise SHIFT
    # bitwise XOR

a = 10
b = 4

print(a & b)
print(a | b)
print(~a)
print(a ^ b)
print(a >> 2)
print(a << 2)
